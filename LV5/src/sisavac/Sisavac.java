package sisavac;

public class Sisavac {

    protected int dob;
    private int tezina;

    public Sisavac() {
        this.dob = 0;
        this.tezina = 0;
    }

    public int getDob() {
        return dob;
    }

    public void setDob(int dob) {
        this.dob = dob;
    }

    public int getTezina() {
        return tezina;
    }

    public void setTezina(int tezina) {
        this.tezina = tezina;
    }
}
